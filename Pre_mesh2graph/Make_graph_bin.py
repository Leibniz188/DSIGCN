# 4. Dataset Generation and Saving Logic
# ==========================================
def process_dataset(csv_path, data_root, save_dir, sample_indices=None):
    if not os.path.exists(save_dir):
        os.makedirs(save_dir)

    df = pd.read_csv(csv_path)
    # Save original indices
    df['folder_id'] = df.index

    if sample_indices is not None:
        df = df.iloc[sample_indices].reset_index(drop=True)

    # Target columns (Last 12)
    target_cols = ['E11', 'E22', 'E33', 'G23', 'G13', 'G12',
                   'v12', 'v13', 'v21', 'v23', 'v31', 'v32']

    print(f"Starting processing of {len(df)} samples...")

    for idx in range(len(df)):
        row = df.iloc[idx]
        real_id = int(row['folder_id'])

        # --- 🔥 Skip existing files to save computation time 🔥 ---
        save_path = os.path.join(save_dir, f"sample_{real_id}.bin")
        if os.path.exists(save_path):
            print(f"[Skipping] Sample {real_id} already exists at {save_path}")
            continue

        # Find target folder
        possible_dirs = [
            os.path.join(data_root, f"particle_3d_{real_id + 1}_mesh"),  # Prioritize your filename pattern
            os.path.join(data_root, f"ellipsoids_3d_{real_id + 1}_mesh"),
            os.path.join(data_root, f"sample_{real_id}"),
            os.path.join(data_root, f"WT-EH{real_id + 1}_mesh"),
            os.path.join(data_root, f"WT-F{real_id + 1}_mesh"),
            os.path.join(data_root, f"copped_fiber_3d_{real_id + 1}_mesh"),
            os.path.join(data_root, f"UnitCell{real_id + 1}_mesh"),
            os.path.join(data_root, f"25UQ25-{real_id + 1}_mesh"),
        ]
        sample_dir = next((d for d in possible_dirs if os.path.exists(d)), None)

        if not sample_dir:
            print(f"[Warning] Skipping ID {real_id}: Directory not found in {data_root}")
            continue

        print(f"Processing Sample {real_id}...")

        # Extract material properties
        mat_props = {
            'E1': row['E_1'], 'nu1': row['V1'],
            'E2': row['E2'], 'nu2': row['V2']
        }

        # Extract Label
        label = torch.tensor(row[target_cols].values.astype(np.float32))

        try:
            processor = RVEProcessor(
                os.path.join(sample_dir, "node.txt"),
                os.path.join(sample_dir, "element.txt"),
                os.path.join(sample_dir, "matrix.txt"),
                mat_props
            )

            g_micro = processor.build_micro_graph()
            g_meso = processor.build_meso_graph()

            # Save
            save_path = os.path.join(save_dir, f"sample_{real_id}.bin")
            # dgl.save_graphs can save graph lists and label dictionaries
            dgl.save_graphs(save_path, [g_micro, g_meso], {'label': label.unsqueeze(0)})

        except Exception as e:
            print(f"Error processing sample {real_id}: {e}")
            import traceback
            traceback.print_exc()


